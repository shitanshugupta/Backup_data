const { validationResult } = require("express-validator");
const conn = require('../db_connection').promise();
const jwt = require("jsonwebtoken");

exports.userpost = async (req, res, next) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(422).json({ errors: errors.array() });
    }
    try {
        if (
            !req.headers.authorization ||
            !req.headers.authorization.startsWith("Bearer") ||
            !req.headers.authorization.split(" ")[1]
        ) {
            return res.status(422).json({
                message: "Please provide the token",
            });
        }

        const theToken = req.headers.authorization.split(' ')[1];
        const decoded = jwt.verify(theToken, 'the-super-strong-secrect');
        let userid = decoded.id;
        let userStatus = decoded.status;
        if (userStatus == "true") {
            await conn.execute(
                "INSERT INTO `post`(`userid`,title,description) VALUES(?,?,?)",
                [userid, req.body.title, req.body.description]
            );
            res.json({
                message: "Post Saved!",
            });
        }else if (userStatus == "false"){
            return res.json({
                message:"Please complete email verification step."
            })
        }
    } catch (err) {
        next(err);
    }
};