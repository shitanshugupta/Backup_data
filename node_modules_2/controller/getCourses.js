const jwt = require('jsonwebtoken');
const conn = require('../db_connection').promise();

exports.getCourses = async (req, res, next) => {

    try {

        const [row] = await conn.execute(
            "SELECT * FROM courses"
        );
        if (row.length > 0) {
            return res.json({
                message: row[0]
            });
        }

    }
    catch (err) {
        next(err);
    }
}